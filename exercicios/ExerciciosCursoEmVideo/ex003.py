# Crie um programa que faça a soma de dois numeros digitados pelo usuario
# Lembrar de com numeros é necessário que o input seja declarado como int
numero1 = int(input('Digite um numero: '))
numero2 = int(input('Digite outro numero: '))
soma = numero1 + numero2
print(f'A soma entre {numero1} e {numero2} é {soma}')